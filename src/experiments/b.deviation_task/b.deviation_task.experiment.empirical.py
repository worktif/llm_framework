def lambda_sin(n_sin, amplitude=5, frequency=1, phase_shift=0, offset=0):
    """
    Generate sinusoidal data for a given number of points.

    Parameters:
    - n_sin: Number of points.
    - amplitude: Maximum amplitude of the sinusoid.
    - frequency: Frequency of the sinusoid.
    - phase_shift: Phase shift of the sinusoid.
    - offset: Vertical offset of the sinusoid.

    Returns:
    - x: Array of x values.
    - y: Array of y values based on the sinusoidal function.
    """
    x = np.linspace(0, 2 * np.pi, n_sin)  # Generate N points over the interval [0, 2*pi]
    y = (amplitude / 2) * (1 + np.sin(frequency * x + phase_shift)) + offset
    return x, y

def optimal_cos(n_cos, amplitude=5, frequency=1, phase_shift=0, offset=0):
    """
    Generate cosinusoidal data for a given number of points.

    Parameters:
    - n_cos: Number of points.
    - amplitude: Maximum amplitude of the cosine.
    - frequency: Frequency of the cosine.
    - phase_shift: Phase shift of the cosine.
    - offset: Vertical offset of the cosine.

    Returns:
    - x: Array of x values.
    - y: Array of y values based on the cosinusoidal function.
    """
    x = np.linspace(0, 2 * np.pi, n_cos)  # Generate N points over the interval [0, 2*pi]
    y = (amplitude / 2) * (1 + np.cos(frequency * x + phase_shift)) + offset
    return x, y

def build_responses_deviation(dialog_by_agent):
    """
    Analyze deviations in task-specific responses generated by an agent.

    Parameters:
    - dialog_by_agent: Dictionary containing agent dialog data.

    Saves:
    - Deviation metrics as CSV.
    - Visualization plots as PNG.
    """
    agent_responses = dialog_by_agent["A1"]  # Using responses from agent A1
    responses = agent_responses["responses"]
    print(responses)

    # Generate sinusoidal coefficients
    [_, Lambda] = lambda_sin(len(responses), 2)
    [__, len_optimal_coefficient] = optimal_cos(len(responses), 2)

    # Create task data
    tasks = {
        "task_id": np.arange(1, len(responses) + 1),
        "len_output": [len(output) for output in responses],
        "len_optimal": [len_optimal_coefficient[i] * len(output) for i, output in enumerate(responses)],
    }

    df = pd.DataFrame(tasks)

    # Calculate deviations \mathcal{D}_{T}(O, T)
    df["deviation"] = Lambda * df["len_optimal"] - df["len_output"]

    # Aggregate metrics
    average_deviation = df["deviation"].mean()
    max_deviation = df["deviation"].max()
    min_deviation = df["deviation"].min()
    median_deviation = df["deviation"].median()
    std_deviation = df["deviation"].std()

    os.makedirs(output_folder, exist_ok=True)

    # Visualization
    plt.figure(figsize=(12, 6))
    plt.bar(df["task_id"], df["deviation"], color='blue', alpha=0.7, label="Deviation")
    plt.plot(df["task_id"], df["len_optimal"], color='green', label="Optimal Length")
    plt.scatter(df["task_id"], df["len_output"], color='red', label="Response Length")
    plt.xlabel("Task ID")
    plt.ylabel("Length/Deviation")
    plt.title(f"Task-specific Agent Deviation Analysis.")
    plt.legend()
    plt.grid()

    output_file = os.path.join(output_folder, f'task_deviation_analysis.chart.png')
    plt.savefig(output_file)

    os.makedirs(output_folder_data, exist_ok=True)

    # Display and save results
    print("Task-specific Deviation Analysis")
    print(df)
    print(f"\nAverage Deviation: {average_deviation}")
    print(f"Max Deviation: {max_deviation}")
    print(f"Min Deviation: {min_deviation}")
    print(f"Median Deviation: {median_deviation}")
    print(f"Std Deviation: {std_deviation}")

    df.to_csv(f"{output_folder_data}/deviation_task.analysis.summary.csv", index=False)

    # plt.show()

    plt.clf()


build_responses_deviation(agent_dialog_by_agent)